<h2>Client</h2>
<p>The name of a computer that can request information from a server.</p>

<h2>HTTP</h2>
<p>HyperText Transfer Procol - This is the standard protocol for exchanging information on the Wolrd Wide Web.  THis allows URL's to be sent from the sever to the client.  HTTP enables the web developers to include hyperlinks, which the user can click on.</p>


<h2>Pre-Processor</h2>
<p>A computer program that modifies data so that it can be inputed and used by another program.</p>


<h2>Query String Parameters</h2>
<p>Part of a URL that contains information to be sent to the server.  They are contained after the URL's path.  Query strings come after a question mark and if there are multiple qery strings, they are separated by an ampersand.</p>


<h2>Request</h2>
<p>An instruction to a computer to perform a task or request information.  A request is made by the user and sent to the sever.</p>


<h2>Response</h2>
<p>The actions taken by the server as the result of a request from a user.</p>


<h2>Server</h2>
<p>A host computer on a network that responds to requests from users.  A server houses websites and is able to execute their links.  It can also refer to the software that makes all of this possible.</p>

